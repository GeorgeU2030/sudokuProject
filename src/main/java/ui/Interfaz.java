    public boolean verify(int row,int col){
        boolean flag=false;
        for(int i=0;i<matrix.length;i++){
            if(i!=col){
               if(matrix[row][i]==matrix[row][col]){
                   flag=true;
                }
            }
           if(i!=row){
               if(matrix[i][col]==matrix[row][col]){
                   flag=true;
               }
           }
        }
        if(matrixverify(row,col)==true||flag==true){
            flag=true;
        }
       return flag;
    }
    public boolean matrixverify(int row,int col){
        boolean flag=false;
        int numberRow=0;
        int numberCol=0;
        
        if(row>=0&&row<=2){
         numberRow=1;
        }
        else if(row>=3&&row<=5){
          numberRow=2;  
        }
        else if(row>=6&&row<=8){
          numberRow=3; 
        }
        if(col>=0&&col<=2){
         numberCol=1;
        }
        else if(col>=3&&col<=5){
          numberCol=2;  
        }
        else if(col>=6&&col<=8){
          numberCol=3; 
        }
        for(int i=(numberRow*3)-3;i<=(numberRow*3)-1;i++){
            for(int j=(numberCol*3)-3;j<=(numberCol*3)-1;j++){
            if(matrix[i][j]==matrix[row][col]){
               if(i!=row&&j!=col){
                flag=true;
            }
        }
        }
    }
         return flag;
    }
    public void reviewWin(){
        boolean flag=false;
        for(int i=0;i<matrix.length;i++){
            for(int j=0;j<matrix[0].length;j++){
                if(verify(i,j)==true){
                    flag=true;
                }
            }
        }
        if(flag==false){
            win.setText("YOU WIN");
        }
        else{
            win.setText(" ");
        }
    }
